namespace CoffeeSh0p
{
    public class Coffee
    {
        public string coffeeName;
        public short price;
        public bool sugar;
        public bool cinnamon;
        public bool sirop;
        public Coffee(string coffeeName, short price, bool sugar, bool cinnamon, bool sirop)
        {
            this.coffeeName = coffeeName;
            this.price = price;
            this.sugar = sugar;
            this.cinnamon = cinnamon;
            this.sirop = sirop;

        }
        
            
    }
}
